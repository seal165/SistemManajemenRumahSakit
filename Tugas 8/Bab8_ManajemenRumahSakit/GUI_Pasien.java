    private void btnTampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilkanActionPerformed
        // TODO add your handling code here:
        try{
            String nama = txtNama.getText();

            int umur = Integer.parseInt(txtUmur.getText());
            String alamat = txtAlamat.getText();

            Pasien pasien;

            if (cmbJenisPasien.getSelectedItem().equals("Rawat Inap")) {
                int lamaInap = Integer.parseInt(txtLamaInap.getText());
                pasien = new PasienRawatInap(nama, umur, alamat, lamaInap);
            } else {
                String jadwalKontrol = txtJadwalKontrol.getText();
                pasien = new PasienRawatJalan(nama, umur, alamat, jadwalKontrol);
            }

            memoInfo.setText(pasien.tampilkanInfo());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Inputan tidak valid, silahkan diisi ulang!");
        }
    }//GEN-LAST:event_btnTampilkanActionPerformed

