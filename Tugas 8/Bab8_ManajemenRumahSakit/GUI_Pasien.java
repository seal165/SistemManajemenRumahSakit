    private void updateFormInput() {
        String jenis = (String) cmbJenisPasien.getSelectedItem();

        if (jenis.equals("Rawat Inap")) {
            txtLamaInap.setEnabled(true);
            txtJadwalKontrol.setEnabled(false);
            txtJadwalKontrol.setText(""); // Kosongkan
        } else {
            txtLamaInap.setEnabled(false);
            txtLamaInap.setText(""); // Kosongkan
            txtJadwalKontrol.setEnabled(true);
        }
    }

