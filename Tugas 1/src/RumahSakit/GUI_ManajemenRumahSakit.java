    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        memoRM.setText(""); // bersihkan area memo dulu

        // Ambil data pasien
        Pasien pasien = new Pasien();
        pasien.dataNama(txtNama.getText());
        int umur = txtUmur.getText().isEmpty() ? 0 : Integer.parseInt(txtUmur.getText());
        pasien.dataUmur(umur);
        pasien.dataDiagnosis(txtDiagnosis.getText());

        // Ambil data dokter
        Dokter dokter = new Dokter();
        dokter.dataDokter(cbDokter.getSelectedItem().toString());

        // Ambil metode pembayaran
        MetodePembayaran metode = new MetodePembayaran();
        metode.dataMetodePembayaran(cbMetodePembayaran.getSelectedItem().toString());

        // Ambil total biaya dan status pembayaran
        Pembayaran pembayaran = new Pembayaran();
        int total = txtTotalBiaya.getText().isEmpty() ? 0 : Integer.parseInt(txtTotalBiaya.getText());
        pembayaran.setTotalBiaya(total);

        // Tampilkan ke memo
        memoRM.append("Rekam Medis Pasien\n");
        memoRM.append("--------------------------------------------------------\n");
        memoRM.append("Nama              : " + pasien.cetakNama() + "\n");
        memoRM.append("Umur              : " + pasien.cetakUmur() + "\n");
        memoRM.append("Diagnosis         : " + pasien.cetakDiagnosis() + "\n");
        memoRM.append("Dokter            : " + dokter.cetakDokter() + "\n");
        memoRM.append("Metode Pembayaran : " + metode.cetakMetodePembayaran() + "\n");
        memoRM.append("Total Biaya       : Rp " + pembayaran.cetakPembayaran() + "\n");
        memoRM.append("Status Awal       : " + pembayaran.cetakStatus() + "\n");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        String dokterDipilih = cbDokter.getSelectedItem().toString();
        int total = 0;

        switch (dokterDipilih) {
            case "Dr. Budi - Jantung":
                total = 700000;
                break;
            case "Dr. Ani - Mata":
                total = 300000;
                break;
            case "Dr. Dika - Anak":
                total = 500000;
                break;
            default:
                total = 0;
        }

        txtTotalBiaya.setText(String.valueOf(total));
    }//GEN-LAST:event_btnHitungActionPerformed

