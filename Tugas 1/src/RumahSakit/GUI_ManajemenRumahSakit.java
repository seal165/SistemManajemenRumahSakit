    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        // Ambil data pasien
        Pasien pasien = new Pasien();
        pasien.dataNama(txtNama.getText());

        // Ambil metode pembayaran
        MetodePembayaran metodepembayaran = new MetodePembayaran();
        metodepembayaran.dataMetodePembayaran(cbMetodePembayaran.getSelectedItem().toString());

        // Set metode pembayaran ke objek pembayaran
        Pembayaran pembayaran = new Pembayaran();
        pembayaran.setMetode(metodepembayaran);

        // Ambil total biaya yang sudah dihitung
        int total = txtTotalBiaya.getText().isEmpty() ? 0 : Integer.parseInt(txtTotalBiaya.getText());
        pembayaran.setTotalBiaya(total);

        // Jalankan konfirmasi
        boolean berhasil = pembayaran.konfirmasiBayar(pasien);

        if (berhasil) {
            // Update status pembayaran
            pembayaran.setStatus("Lunas");  // Misalnya mengubah status menjadi "Lunas"
            JOptionPane.showMessageDialog(this, "Pembayaran berhasil!");

            // Tampilkan atau perbarui status pembayaran di UI
            memoRM.append("Status Akhir       : Lunas\n");
        } else {
            JOptionPane.showMessageDialog(this, "Pembayaran dibatalkan.");
        }
    }//GEN-LAST:event_btnBayarActionPerformed

